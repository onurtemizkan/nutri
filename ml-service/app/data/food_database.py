def get_micronutrient_estimates(
    food_class: str, portion_weight: float, category: Optional[FoodCategory] = None
) -> Dict[str, Optional[float]]:
    """
    Get estimated micronutrients for a food item.

    Args:
        food_class: Food class/name
        portion_weight: Weight in grams
        category: Food category (if known)

    Returns:
        Dict of micronutrient values scaled to portion weight
    """
    # Get food entry if available
    entry = FOOD_DATABASE.get(food_class)
    if entry:
        category = entry.category

    # Use category estimates if available
    if category and category in MICRONUTRIENT_CATEGORY_ESTIMATES:
        estimates = MICRONUTRIENT_CATEGORY_ESTIMATES[category]
    else:
        estimates = MICRONUTRIENT_CATEGORY_ESTIMATES[FoodCategory.UNKNOWN]

    # Scale from per-100g to portion weight
    scale = portion_weight / 100.0

    return {
        key: round(value * scale, 2) if value is not None else None
        for key, value in estimates.items()
    }


