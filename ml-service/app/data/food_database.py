@dataclass
class FoodEntry:
    """Complete food database entry"""

    name: str
    display_name: str
    category: FoodCategory
    density: float  # g/cm³
    shape_factor: float  # 0-1, accounts for non-cuboid shapes
    serving_size: str
    serving_weight: float  # grams
    calories: float  # per serving
    protein: float  # grams per serving
    carbs: float  # grams per serving
    fat: float  # grams per serving

    # Extended macros
    fiber: Optional[float] = None
    sugar: Optional[float] = None

    # Fat breakdown
    saturated_fat: Optional[float] = None
    trans_fat: Optional[float] = None  # grams
    cholesterol: Optional[float] = None  # mg

    # Minerals (mg per serving)
    sodium: Optional[float] = None  # mg
    potassium: Optional[float] = None  # mg
    calcium: Optional[float] = None  # mg
    iron: Optional[float] = None  # mg
    magnesium: Optional[float] = None  # mg
    zinc: Optional[float] = None  # mg
    phosphorus: Optional[float] = None  # mg

    # Vitamins (various units per serving)
    vitamin_a: Optional[float] = None  # mcg RAE
    vitamin_c: Optional[float] = None  # mg
    vitamin_d: Optional[float] = None  # mcg
    vitamin_e: Optional[float] = None  # mg
    vitamin_k: Optional[float] = None  # mcg
    vitamin_b6: Optional[float] = None  # mg
    vitamin_b12: Optional[float] = None  # mcg
    folate: Optional[float] = None  # mcg DFE
    thiamin: Optional[float] = None  # mg (B1)
    riboflavin: Optional[float] = None  # mg (B2)
    niacin: Optional[float] = None  # mg (B3)

    # Amino acids (mg per serving)
    lysine: Optional[float] = None  # mg per serving - essential amino acid
    arginine: Optional[
        float
    ] = None  # mg per serving - conditionally essential amino acid

    # Metadata
    fdc_id: Optional[str] = None  # USDA FoodData Central ID
    aliases: Optional[List[str]] = None  # Alternative names for fuzzy matching
    default_cooking_method: CookingMethod = CookingMethod.RAW

    def __post_init__(self):
        if self.aliases is None:
            self.aliases = []


