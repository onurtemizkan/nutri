from typing import Optional, Dict, List, Union
from dataclasses import dataclass
from enum import Enum


class FoodCategory(str, Enum):
    """Food category classifications"""

    FRUIT = "fruit"
    VEGETABLE = "vegetable"
    PROTEIN = "protein"
    GRAIN = "grain"
    DAIRY = "dairy"
    BEVERAGE = "beverage"
    BAKED = "baked"
    SNACK = "snack"
    MIXED = "mixed"
    LEGUME = "legume"
    SEAFOOD = "seafood"
    NUT = "nut"
    CONDIMENT = "condiment"
    UNKNOWN = "unknown"


class CookingMethod(str, Enum):
    """Cooking method classifications"""

    RAW = "raw"
    COOKED = "cooked"
    BOILED = "boiled"
    STEAMED = "steamed"
    GRILLED = "grilled"
    FRIED = "fried"
    BAKED = "baked"
    ROASTED = "roasted"
    SAUTEED = "sauteed"
    POACHED = "poached"


@dataclass
class CookingModifier:
    """Modifiers for cooking method effects on weight/nutrition"""

    weight_multiplier: float  # How weight changes (moisture loss/gain)
    calorie_multiplier: float  # How calories change (oil absorption, etc.)
    description: str


